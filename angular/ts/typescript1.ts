console.log("---arithmetic---")

// Addition
let sum: number = 5 + 3; // sum = 8
console.log("Sum:", sum);

// Subtraction
let difference: number = 10 - 4; // difference = 6
console.log("Difference:", difference);

// Multiplication
let product: number = 2 * 6; // product = 12
console.log("Product:", product);

// Division
let quotient: number = 20 / 5; // quotient = 4
console.log("Quotient:", quotient);

// Modulus (Remainder)
let remainder: number = 10 % 3; // remainder = 1 (10 divided by 3 equals 3 with a remainder of 1)
console.log("Remainder:", remainder);

// Increment
let x: number = 5;
x++; // Increment x by 1, now x = 6
console.log("Incremented x:", x);

// Decrement
let y: number = 8;
y--; // Decrement y by 1, now y = 7
console.log("Decremented y:", y);
